					<h2>About us</h2>
					<p>Kenya Youth Assembly Foundation (KYAF) is a public non-profit and non-partisan foundation dedicated to the growth and strengthening of youth involvement in policy formulation, democratic engagements and leadership. This is aimed at not only ensuring youth participation in decision making, but also promoting good governance, improving service delivery, strengthening democratic principles and institutions, and enhancing general citizen participation. KYAF is primarily focused at establishing, developing, maintaining and granting aid in cash or in kind to County Youth Assemblies based in Kenya. The Youth Assemblies implement their programs using parliamentary sessions that model the official County Assemblies in Kenya. Additionally, their activities follow an established policy cycle created by KYAF that involves thorough research, lobbying, citizen public meetings (ward youth barazas), public awareness and advocacy programs among others. </p>
					<p>KYAF was registered in January 2020. However, its origin started as a Community Based Organization known as Kisauni Counstituency Youth Parliament founded in 2009 in Kisauni constituency that expanded to become Mombasa Youth Assembly in 2013 covering all Mombasa County constituencies. Its new mandate now covers the entire Kenya all because of the growing demand that we have received from Kenyan Youths from different corners of the country. The youths have expressed their feeling of marginalisation, are eager to be involved in policy formulation and leadership, there is an increasing need for good governance and expansion of the democratic space. For instance, in Counties that lack an opposition, we provide a platform for the alternative voice to be heard and substantive community issues to be researched and debated. In a nutshell KYAF is founded under the 6 pillars of: Humanity (empowering the youth); Knowledge (awareness and research); Advocacy and Activism (lobbying, mobilisation and action); Sustainability (political, social and economic); Integrity (good governance); and Collaboration. </p>
